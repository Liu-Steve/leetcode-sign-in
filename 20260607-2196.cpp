class Solution {
public:
    TreeNode* createBinaryTree(vector<vector<int>>& descriptions) {
        unordered_map<int, TreeNode*> mp;
        for (auto& d : descriptions) {
            mp[d[1]] = new TreeNode(d[1]);
        }
        TreeNode* ans = nullptr;
        for (auto& d : descriptions) {
            if (!mp.count(d[0])) {
                ans = new TreeNode(d[0]);
                mp[d[0]] = ans;
            }
            if (d[2]) {
                mp[d[0]]->left = mp[d[1]];
            } else {
                mp[d[0]]->right = mp[d[1]];
            }
        }
        return ans;
    }
};

