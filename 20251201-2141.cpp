constexpr long long MOD = 1000000007LL;

class Solution
{
public:
    long long maxRunTime(int n, vector<int> &batteries)
    {
        long long l = 0;
        long long r = 100000000000010ll;
        auto v = [&](long long t) -> bool
        {
            long long sum = 0;
            for (long long b : batteries)
                sum += min(b, t);
            return sum >= t * n;
        };
        while (l + 1 < r)
        {
            long long m = (l + r) / 2;
            if (v(m))
                l = m;
            else
                r = m;
        }
        return l;
    }
};

