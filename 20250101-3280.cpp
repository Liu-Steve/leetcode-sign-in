class Solution
{
public:
    string convertDateToBinary(string date)
    {
        int year = (date[0] - '0') * 1000 + (date[1] - '0') * 100 + (date[2] - '0') * 10 + (date[3] - '0');
        int month = (date[5] - '0') * 10 + (date[6] - '0');
        int day = (date[8] - '0') * 10 + (date[9] - '0');
        stringstream ss;
        auto binary = [&](int num)
        {
            vector<int> v;
            while (num)
            {
                v.push_back(num & 1);
                num >>= 1;
            }
            for (auto i = v.rbegin(); i != v.rend(); ++i)
                ss << *i;
        };
        binary(year);
        ss << '-';
        binary(month);
        ss << '-';
        binary(day);
        return ss.str();
    }
};

