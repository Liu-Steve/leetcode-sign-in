class Solution
{
public:
    int maximumRobots(vector<int> &chargeTimes, vector<int> &runningCosts, long long budget)
    {
        int n = chargeTimes.size();
        int l = 0;
        int r = 0;
        long long sum = 0;
        int ans = 0;
        deque<int> q;
        for (; r < n; ++r)
        {
            while (!q.empty() && chargeTimes[q.back()] <= chargeTimes[r])
                q.pop_back();
            q.push_back(r);
            sum += runningCosts[r];
            while (!q.empty() && chargeTimes[q.front()] + (r - l + 1) * sum > budget)
            {
                if (q.front() == l)
                    q.pop_front();
                sum -= runningCosts[l++];
            }
            ans = max(ans, r - l + 1);
        }
        return ans;
    }
};

