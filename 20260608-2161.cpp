class Solution {
public:
    vector<int> pivotArray(vector<int>& nums, int pivot) {
        int n = nums.size();
        vector<int> ans(n, pivot);
        int p = 0;
        for (int i = 0; i < n; ++i) {
            if (nums[i] < pivot) {
                ans[p] = nums[i];
                p++;
            }
        }
        p = n - 1;
        for (int i = n - 1; i >= 0; --i) {
            if (nums[i] > pivot) {
                ans[p] = nums[i];
                p--;
            }
        }
        return ans;
    }
};

