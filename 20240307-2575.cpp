class Solution
{
public:
    vector<int> divisibilityArray(string word, int m)
    {
        long long num = 0;
        int n = word.size();
        vector<int> ans(n);
        for (int i = 0; i < n; ++i)
        {
            num *= 10;
            num += word[i] - '0';
            num %= m;
            ans[i] = (num == 0);
        }
        return ans;
    }
};

