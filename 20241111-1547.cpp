class Solution
{
public:
    int minCost(int n, vector<int> &cuts)
    {
        cuts.push_back(0);
        cuts.push_back(n);
        sort(cuts.begin(), cuts.end());
        int m = cuts.size();
        vector<vector<int>> dp(m, vector<int>(m));
        for (int i = 0; i < m; ++i)
        {
            for (int j = i - 2; j >= 0; --j)
            {
                int cost = INT32_MAX;
                for (int k = j + 1; k <= i - 1; ++k)
                {
                    cost = min(cost, dp[j][k] + dp[k][i]);
                }
                dp[j][i] = cost + cuts[i] - cuts[j];
            }
        }
        return dp[0][m - 1];
    }
};

