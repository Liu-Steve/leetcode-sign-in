class Solution
{
public:
    long long maxScore(vector<int> &nums, int x)
    {
        vector<long long> dp(2, -0x7f7f7f7f);
        dp[nums[0] % 2] = nums[0];
        for (int i = 1; i < nums.size(); ++i)
        {
            long long num = nums[i];
            int idx = num % 2;
            dp[idx] = max(dp[idx] + num, dp[!idx] + num - x);
        }
        return max(dp[0], dp[1]);
    }
};

