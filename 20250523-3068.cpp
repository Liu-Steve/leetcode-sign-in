class Solution
{
public:
    long long maximumValueSum(vector<int> &nums, int k, vector<vector<int>> &edges)
    {
        long long f0 = 0;
        long long f1 = INT32_MIN;
        for (int num : nums)
        {
            long long new_f0 = max(f0 + num, f1 + (num ^ k));
            long long new_f1 = max(f1 + num, f0 + (num ^ k));
            f0 = new_f0;
            f1 = new_f1;
        }
        return f0;
    }
};

