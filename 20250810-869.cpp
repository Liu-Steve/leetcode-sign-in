class Solution
{
public:
    bool reorderedPowerOf2(int n)
    {
        auto reorder = [&](int num) -> string
        {
            vector<int> digit;
            while (num)
            {
                digit.push_back(num % 10);
                num /= 10;
            }
            sort(digit.begin(), digit.end());
            stringstream ss;
            for (int d : digit)
                ss << d;
            return ss.str();
        };
        unordered_set<string> s;
        for (int i = 1; i > 0; i <<= 1)
            s.insert(reorder(i));
        return s.count(reorder(n));
    }
};

